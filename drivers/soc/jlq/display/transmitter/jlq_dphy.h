static void jlq_dphy_get_transition_time(struct dw_mipi_dsi *dsi)
{
	struct dphy_hs_tx_timing *timing;
	u32 lane_mbps = dsi->lane_mbps;

	timing = jlq_dphy_get_hs_tx_timing(lane_mbps);
	if (!timing) {
		dev_err(dsi->dev, "invalid lane_mbps:0x%x\n", lane_mbps);
		return;
	}

	dsi->lp2hs_time = timing->t_lpx + timing->t_hs_prepare + timing->t_hs_zero + 10;
	dsi->hs2lp_time = timing->t_hs_trail + timing->t_hs_exit + 5;
	dsi->clklp2hs_time = timing->t_lpx + timing->t_clk_prepare + timing->t_clk_zero + 10;
	dsi->clkhs2lp_time = timing->t_clk_trail + timing->t_hs_exit + 5;
}

/* Get the adjusted pll out value */
static void jlq_dphy_adjusted_pllout(struct dw_mipi_dsi *dsi)
{
	u32 lane_mbps = dsi->lane_mbps;
	/* Add 20% to pll out to be higher than pixel bw (burst mode only) */
#if ADD_EXTRA_DPHY_BAND_WIDTH
	lane_mbps = (lane_mbps * 12) / 10;
#endif
	lane_mbps -= lane_mbps % 10;

	if (lane_mbps > LANE_MBPS_MAX) {
		lane_mbps = LANE_MBPS_MAX;
		dev_err(dsi->dev, "use max phy mbps\n");
	}

	if (lane_mbps < LANE_MBPS_MIN) {
		lane_mbps = LANE_MBPS_MIN;
		dev_err(dsi->dev, "use min phy mbps\n");
	}

	dsi->lane_mbps = lane_mbps;
}

static inline u32 dphy_read(struct dw_mipi_dsi *dsi, u32 reg)
{
	return readl(dsi->dphy_base + reg);
}

static inline void dphy_write(struct dw_mipi_dsi *dsi, u32 reg, u32 val)
{
	writel(val, dsi->dphy_base + reg);
}

static inline void dphy_write_mask(struct dw_mipi_dsi *dsi,
			u32 reg, u32 val, u32 mask)
{
	u32 tmp = dphy_read(dsi, reg);

	tmp &= (~mask);
	dphy_write(dsi, reg, val | tmp);
}

static void jlq_dphy_hs_timing_init(struct dw_mipi_dsi *dsi)
{
	u32 val;
	struct dphy_hs_tx_timing *timing;

	timing = jlq_dphy_get_hs_tx_timing(dsi->lane_mbps);
	if (!timing) {
		dev_err(dsi->dev, "invalid bitclock rate:0x%x\n",
			dsi->lane_mbps);

		return;
	}

	val = timing->t_clk_prepare;
	dphy_write(dsi, DIS_DPHY_M_TCLKPRPRCTL, val);

	val = timing->t_clk_zero;
	dphy_write(dsi, DIS_DPHY_M_TCLKZEROCTL, val);

	val = timing->t_clk__post;
	dphy_write(dsi, DIS_DPHY_M_TCLKPOSTCTL, val);

	val = timing->t_clk_trail;
	dphy_write(dsi, DIS_DPHY_M_TCLKTRAILCTL, val);

	val = timing->t_hs_prepare;
	dphy_write(dsi, DIS_DPHY_M_THSPRPRCTL, val);

	val = timing->t_hs_zero;
	dphy_write(dsi, DIS_DPHY_M_THSZEROCTL, val);

	val = timing->t_hs_trail;
	dphy_write(dsi, DIS_DPHY_M_THSTRAILCTL, val);

	val = timing->t_lpx;
	dphy_write(dsi, DIS_DPHY_M_TLPXCTL, val);

	val = timing->t_hs_exit;
	dphy_write(dsi, DIS_DPHY_M_THSEXITCTL, val);

}

static void jlq_dphy_ulps_timing_init(struct dw_mipi_dsi *dsi)
{
	struct dphy_ulps_exit_timing *ulps_timing;
	u32 esc_clk_rate = (dsi->lane_mbps >> 3) / dsi->esc_clk_division;

	ulps_timing = jlq_dphy_get_ulps_exit_timing(esc_clk_rate);
	if (!ulps_timing) {
		dev_err(dsi->dev, "invalid esc_clk_rate:0x%x\n",
			esc_clk_rate);
	} else
		dphy_write(dsi, DIS_DPHY_B_DPHYCTL_L, ulps_timing->twakeup);
}

static void jlq_dphy_pll_init(struct dw_mipi_dsi *dsi)
{
	u32 val;
	unsigned long pll_refclk_rate;
	struct pll_pmsk *pmsk;

	pmsk = jlq_dphy_get_pll_pmsk(dsi->lane_mbps);
	if (!pmsk) {
		dev_err(dsi->dev, "invalid byteclock rate:0x%x\n",
			dsi->lane_mbps);

		return;
	}

	val = pmsk->pll_s | (pmsk->pll_m << 4) | (pmsk->pll_p << 16);
	dphy_write(dsi, DIS_DPHY_M_PLLPMS, val);

	val = (pmsk->pll_k & 0xffff) << 8;
	dphy_write(dsi, DIS_DPHY_M_PLLCTL_H, val);

	pll_refclk_rate = clk_get_rate(dsi->pllref_clk);
	pll_refclk_rate /= 1000000;
	val = pmsk->pll_p / pll_refclk_rate + 1;
	udelay(val);

	//pll enable
	val = (0x1 << 12);
	dphy_write(dsi, DIS_DPHY_CTRL, val);
	//pll lock time
	val = 3000 * pmsk->pll_p / pll_refclk_rate;
	udelay(val);
}

static void jlq_dphy_lane_release(struct dw_mipi_dsi *dsi)
{
	//dphy lane enable release
	dphy_write_mask(dsi, DIS_DPHY_CTRL, 0x1 << 8, 0x1 << 8);
	//lp regulator initial time
	udelay(200);
}

static void jlq_dphy_init(struct dw_mipi_dsi *dsi)
{
	jlq_dphy_hs_timing_init(dsi);
	jlq_dphy_ulps_timing_init(dsi);
	jlq_dphy_pll_init(dsi);
	jlq_dphy_lane_release(dsi);
}

static void jlq_dphy_deinit(struct dw_mipi_dsi *dsi)
{
	dphy_write(dsi, DIS_DPHY_CTRL, 0x0);
}

