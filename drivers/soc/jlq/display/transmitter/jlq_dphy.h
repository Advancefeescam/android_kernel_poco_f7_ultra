	dev_err(dsi->dev, "reg[0x%04x]=0x%08x\n", 0xa0,
		dphy_read(dsi, 0xa0));

	for (reg = 0xa8; reg < 0xb4;) {
		dev_err(dsi->dev, "reg[0x%04x]=0x%08x\n", reg,
			dphy_read(dsi, reg));
		reg += 4;
	}
